package org.gvnix.dynamic.configuration.roo.addon.entity;

public class DynComponent {
  
  private String name;

  
  public DynComponent() {
    super();
  }

  public DynComponent(String name) {
    super();
    this.name = name;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
